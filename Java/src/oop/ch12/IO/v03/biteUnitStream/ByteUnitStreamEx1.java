package oop.ch12.IO.v03.biteUnitStream;

public class ByteUnitStreamEx1 {

	/*
	 * 
	 * # 바이트 단위 스트림
	 * 
	 *   - InputStream : 바이트 단위 입력 스트림 최상위 클래스
	 *     
	 *   - OutputStream : 바이트 단위 출력 스트림 최상위 클래스
	 *     
	 *   - 추상 메서드를 포함한 추상 클래스로 하위 클래스가 구현하여 사용
	 *   
	 *   - 주요 하위 클래스
	 *   
	 *     1. InputStream 하위 클래스
	 *     
	 *       1.1 FileInputStream*
	 *       
	 *         - 파일에서 바이트 단위로 자료를 읽음
	 *         
	 *       1.2 ByteArrayInputStream
	 *       
	 *         - Byte 배열 메모리에서 바이트 단위로 자료를 읽음
	 *         
	 *       1.3 FilterInputStream
	 *       
	 *         - 기반 스트림에서 자료를 읽을 때 추가 기능을 제공하는 보조 스트림의 상위 클래스
	 *     
	 *     
	 *     2. OutputStream 하위 클래스
	 *     
	 *       2.1 FileOutputStream*
	 *       
	 *         - 바이트 단위로 파일에 자료를 씀
	 *         
	 *       2.2 ByteArrayOutputStream
	 *       
	 *         - Byte 배열에 바이트 단위로 자료를 씀
	 *         
	 *       2.3 FilterOutputStream
	 *       
	 *         - 기반 스트림에서 자료를 쓸 때 추가 기능을 제공하는 보조 스트림의 상위 클래스
	 * 
	 */
	
}
